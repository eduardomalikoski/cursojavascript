// maneira antiga
let exp = Math.pow(2, 3) // O primeiro parâmetro é a base e o segundo o expoente
console.log(exp);

let exp2 = 2 ** 3 // O primeiro valor representa a base e o segundo o expoente
console.log(exp2); // 2 elevado a 3 igual a 8

let base = 2
//base **= 3 é a mesma coisa que: base = base ** 3
base **= 3 // Eleva o valor armazenado na base à terceira potência
console.log(base); // 2 elevado a 3 igual a 8

let expNegativo = 2 ** -3 // Com expoente negativo
console.log(expNegativo); // 2 elevado a -3 igual a 0.125
